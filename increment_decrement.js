// Declare a variable and set it.
let a = 10;
let b = 20;

// Increment the variable.
a++;
b--;

// Display the output 
console.log(a);
console.log(b);

// More Practice...
let gainedDollar = 3;
let lost_dollar = 20;

gainedDollar++;
lost_dollar--;

console.log("Value for gainedDollar:", gainedDollar);
console.log("Value for lost_dollar:", lost_dollar);

